class Film {
  constructor(title, year, country, actors, genres, synopsys, poster, rating) {
    this.title = title;
    this.year = year;
    this.country = country;
    this.actors = actors;
    this.genres = genres;
    this.synopsys = synopsys;
    this.poster = poster;
    this.rating = rating;
  }
}